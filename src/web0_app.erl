-module(web0_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {ok, _} = application:ensure_all_started(cowboy),
    web0_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

